# 1. Открываем файл для чтения.
# Конструкция 'with' полезна тем, что сама закроет файл после выполнения кода.
with open("C:\\Users\\UserPC\\Desktop\\alsuege\\alsu\\9_27764.csv") as f:
    count = 0  # Переменная-счетчик для подходящих строк

    for x in f:
        # x — это сейчас одна строка текста, например: "11;91;34;33;48\n"

        # 2. Очистка и преобразование данных:
        # .strip() — удаляет невидимые символы в конце (например, перенос строки \n).
        # .split(';') — РАЗРЕЗАЕТ строку по точке с запятой. Получаем список строк: ["11", "91", "34", "33", "48"]
        # map(int, ...) — ПРИМЕНЯЕТ функцию int (целое число) к каждому элементу списка.
        # list(...) — превращает результат map обратно в нормальный СПИСОК, с которым можно работать.
        row = list(map(int, x.strip().split(';')))

        # 3. Сортировка:
        # row.sort() — расставляет числа от самого маленького до самого большого.
        # После этого: row[0] всегда МИНИМУМ, а row[4] всегда МАКСИМУМ.
        row.sort()

        # 4. Проверка на уникальность (Условие 1):
        # set(row) — создает МНОЖЕСТВО. В множестве не может быть повторяющихся элементов.
        # Если в строке были дубли (например, 11 и 11), множество их «склеит» в одну 11.
        # len(...) — считает количество элементов. Если их осталось 5, значит, все были разными.

        all_different = set(row)

        # 5. Проверка суммы (Условие 2):
        # Сумма MIN и MAX: (row[0] + row[4])
        # Сумма оставшихся: это элементы row[1], row[2], row[3] (индексы от 1 до 3 включительно)
        # sum(row[1:4]) — функция sum складывает числа в указанном срезе.
        min_max_doubled = 2 * (row[0] + row[4])
        others_sum = sum(row[1:4])

        # 6. Итоговое решение по строке:
        # Если оба условия истинны (True), увеличиваем счетчик.
        if len(all_different)==5 and min_max_doubled == others_sum:
            count += 1

print("Количество строк:", count)