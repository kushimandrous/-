from itertools import product
# Создаем счетчик для подходящих кодов
count = 0

# Генерируем все возможные 8-буквенные комбинации из нашего алфавита
# repeat=8 означает длину слова
for s in product('ИГОРЬ', repeat=8):
    # s — это кортеж из букв, например ('И', 'Г', 'О', 'Р', 'Ь', 'И', 'Г', 'О')
    # Склеим его в строку для удобства
    word = "".join(s)

    # Проверяем условия задачи:
    # 1. 'О' встречается ровно 1 раз
    # 2. 'Ь' встречается ровно 1 раз
    # 3. 'Ь' не на первом месте (индекс 0)
    if word.count('О') == 1 and word.count('Ь') == 1:
        if word[0] != 'Ь':
            count += 1

print(count)


